export const SUPPORTED_LANGS = ['en', 'uk', 'es', 'pl', 'de'];

export function t(key, lang = 'en', vars = {}) {
  const dict = T[lang] || T.en;
  let str = dict[key] ?? T.en[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replaceAll(`{${k}}`, String(v));
  }
  return str;
}

function pluralForm(n, lang) {
  if (lang === 'uk' || lang === 'pl') {
    const m10 = n % 10, m100 = n % 100;
    if (m10 === 1 && m100 !== 11) return 'one';
    if (m10 >= 2 && m10 <= 4 && (m100 < 12 || m100 > 14)) return 'few';
    return 'other';
  }
  return n === 1 ? 'one' : 'other';
}

export function plural(n, lang, baseKey) {
  return t(`${baseKey}_${pluralForm(n, lang)}`, lang);
}

export function detectLang(initial) {
  const candidate = (initial || '').slice(0, 2).toLowerCase();
  return SUPPORTED_LANGS.includes(candidate) ? candidate : 'en';
}
